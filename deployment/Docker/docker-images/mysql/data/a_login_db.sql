DROP DATABASE IF EXISTS login_db;
CREATE DATABASE IF NOT EXISTS login_db DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;


CREATE TABLE IF NOT EXISTS login_db.user (
id INTEGER UNSIGNED NOT NULL AUTO_INCREMENT,
username VARCHAR(30) UNIQUE NOT NULL,
email VARCHAR(30) UNIQUE NOT NULL,
`password_hash` VARCHAR(128) NOT NULL,
PRIMARY KEY (id)
)ENGINE=INNODB;

CREATE USER 'spring-server'@'192.168.100' IDENTIFIED BY '@Spring';
GRANT ALL PRIVILEGES ON login_db.* TO 'spring-server'@'192.168.100' ;   



